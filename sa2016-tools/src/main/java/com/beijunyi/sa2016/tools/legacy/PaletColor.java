package com.beijunyi.sa2016.tools.legacy;

public class PaletColor {

  private final int blue;
  private final int green;
  private final int red;

  public PaletColor(int red, int green, int blue) {
    this.blue = blue;
    this.green = green;
    this.red = red;
  }

  public int getBlue() {
    return blue;
  }

  public int getGreen() {
    return green;
  }

  public int getRed() {
    return red;
  }

}
